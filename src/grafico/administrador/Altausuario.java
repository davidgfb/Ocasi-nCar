    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        String nombre = txt_nombre.getText();
        String dni = txt_dni.getText();
        String correo = txt_correo.getText();
        String contraseña = txt_contraseña.getText();
        String relacion = txt_relacion.getText();
        String telefono = txt_telefono.getText();
        Cliente c = new Cliente(nombre, dni, relacion, correo, contraseña, telefono);
        if (e.getClientes().containsKey(correo)) {
            JOptionPane.showMessageDialog(this, "Usuario ya registrado");
        } else {
            e.getClientes().put(correo, c);
            e.guardarDatosClientes();
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente");
        }

    }//GEN-LAST:event_registrarActionPerformed


    
    private void txt_nombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_nombreKeyTyped
        char c = evt.getKeyChar();
        if ((c < 'a' || c > 'z') && (c < 'A' || c > 'Z') && (c < ' ' || c > ' ')) {
            evt.consume();
        }
        
    }//GEN-LAST:event_txt_nombreKeyTyped


    
    private void txt_telefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_telefonoKeyTyped
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
        
    }//GEN-LAST:event_txt_telefonoKeyTyped



